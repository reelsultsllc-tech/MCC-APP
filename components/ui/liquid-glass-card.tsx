function LiquidCard({ className, ...props }: React.ComponentProps<"div">) {
  return (
    <div>
      <div
        data-slot="card"
        style={{ backdropFilter: 'url("#card-glass")' }}
        className={cn(
          "text-[#241014] bg-white/70 flex flex-col gap-6 rounded-xl border border-[#E7E2E1] py-6",
          "shadow-[0_0_6px_rgba(0,0,0,0.03),0_2px_6px_rgba(0,0,0,0.08),inset_3px_3px_0.5px_-3px_rgba(0,0,0,0.06),inset_-3px_-3px_0.5px_-3px_rgba(0,0,0,0.04),inset_0_0_6px_6px_rgba(0,0,0,0.02),0_0_12px_rgba(122,30,44,0.06)]",
          "transition-all",
          className
        )}
        {...props}
      />
      <GlassFilter />
    </div>
  )
}

function GlassFilter() {
  return (
    <svg className="hidden">
      <defs>
        <filter id="card-glass" x="0%" y="0%" width="100%" height="100%" colorInterpolationFilters="sRGB">
          <feTurbulence type="fractalNoise" baseFrequency="0.02 0.02" numOctaves="1" seed="1" result="turbulence" />
          <feGaussianBlur in="turbulence" stdDeviation="2" result="blurredNoise" />
          <feDisplacementMap in="SourceGraphic" in2="blurredNoise" scale="80" xChannelSelector="R" yChannelSelector="B" result="displaced" />
          <feGaussianBlur in="displaced" stdDeviation="3" result="finalBlur" />
          <feComposite in="finalBlur" in2="finalBlur" operator="over" />
        </filter>
      </defs>
    </svg>
  )
}

export { Card, CardHeader, CardFooter, CardTitle, CardAction, CardDescription, CardContent, LiquidCard }
